package com.zhg.entity;

public class Music {
	private int id;
	private String name;
	private String singer;
	private String zuoci;
	private String zuoqu;
	private String zhuanji;
	private String zjPath;
	private String musicPath;
	private String type;
	private String time;
	private int downCount;
	private int favCount;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSinger() {
		return singer;
	}
	public void setSinger(String singer) {
		this.singer = singer;
	}
	public String getZuoci() {
		return zuoci;
	}
	public void setZuoci(String zuoci) {
		this.zuoci = zuoci;
	}
	public String getZuoqu() {
		return zuoqu;
	}
	public void setZuoqu(String zuoqu) {
		this.zuoqu = zuoqu;
	}
	public String getZhuanji() {
		return zhuanji;
	}
	public void setZhuanji(String zhuanji) {
		this.zhuanji = zhuanji;
	}
	public String getZjPath() {
		return zjPath;
	}
	public void setZjPath(String zjPath) {
		this.zjPath = zjPath;
	}
	public String getMusicPath() {
		return musicPath;
	}
	public void setMusicPath(String musicPath) {
		this.musicPath = musicPath;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public int getDownCount() {
		return downCount;
	}
	public void setDownCount(int downCount) {
		this.downCount = downCount;
	}
	public int getFavCount() {
		return favCount;
	}
	public void setFavCount(int favCount) {
		this.favCount = favCount;
	}
	
	
}
